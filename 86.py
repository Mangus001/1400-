N = int(input("Введите четырехзначное число: "))
if N < 1000 or N > 9999:
    print("Ошибка: число должно быть четырехзначным.")
    exit()

a1 = N // 1000
a2 = (N // 100) % 10
a3 = (N // 10) % 10
a4 = N % 10

reversed_num = a4 * 1000 + a3 * 100 + a2 * 10 + a1
print("Обратное число:", reversed_num)
