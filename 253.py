k = int(input("Введите номер дня (1-365): "))
d = int(input("Введите число d (1-7), где 1 — понедельник: "))

weekday_number = (d + (k - 1)) % 7
weekdays = {
    1: "понедельник",
    2: "вторник",
    3: "среда",
    4: "четверг",
    5: "пятница",
    6: "суббота",
    0: "воскресенье"
}
print(weekdays.get(weekday_number, "Некорректный номер"))
